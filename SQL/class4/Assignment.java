
public class Assignment {
    public static void main(String[] args) {
        //Assignment Operators
        int x =10;
        int y=20;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}
