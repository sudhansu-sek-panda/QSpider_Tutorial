

public class Relational {
    public static void main(String[] args) {
     System.out.println(20<10);
     // Relational Operator gives Boolean Value
     System.out.println(10<20.5);
     // It works with float primitive type.
     System.out.println('a' < 98); // a's value is 97
     // It works with character primitive type
    //  System.out.println(true>false);
     // It doesn't works with Boolean primitive type. (The only type where Realational operator doesn't work and provide CE)

    }
}
